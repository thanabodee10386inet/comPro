numbers = [4, 2, 3, 1, 5]
numbers.sort()  #เรียงลำดับ numbers จากน้อยไปมาก
numbers.reverse()  #กลับรายการ numbers
print(f"Reversed numbers: {numbers}")  #ผลลัพธ์จะเป็น [5, 1, 3, 2, 4]


