matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

matrix[0][1] = 10  # เปลี่ยนค่าในตำแหน่ง (0, 1) ของ matrix เป็น 10
print(matrix)  # แสดงผลลัพธ์จะเป็น [[1, 10, 3], [4, 5, 6], [7, 8, 9]]

for row in matrix:
    for element in row:
        print(element, end=' ')  # แสดงผลแต่ละองค์ประกอบใน matrix โดยไม่ขึ้นบรรทัดใหม่
    print()  # ขึ้นบรรทัดใหม่หลังจากแสดงแต่ละแถว